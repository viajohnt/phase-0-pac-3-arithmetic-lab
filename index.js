function add(a, b) {
    return a+b;
}
function subtract() {
    return a-b;
}
function multiply() {
    return a*b;
}
function divide() {
    return a/b;
}
function increment(n) {
    return n + 1;
  }
function decrement(n) {
    return n - 1;
  }
function makeInt(n) {
    return parseInt(n);
  }
function preserveDecimal(n) {
    return parseFloat(n);
  }